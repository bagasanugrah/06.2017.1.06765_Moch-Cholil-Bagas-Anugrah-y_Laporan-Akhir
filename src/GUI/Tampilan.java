    private void viewData(){
    DefaultTableModel tabelku = new DefaultTableModel();
    tabelku.addColumn("id pengawas");
    tabelku.addColumn("Nama Vendor");
    tabelku.addColumn("Proyek");
    tabelku.addColumn("Material");
    tabelku.addColumn("Total tagihan");
    try{
        String sql = "Select * from pengawas";
        pst = koneksi.prepareStatement(sql);
        rs = pst.executeQuery(sql);
        while (rs.next()) {
            tabelku.addRow(new Object[]{
                rs.getString(1),
                rs.getString(2),
                rs.getString(3),
                rs.getString(4),
                rs.getString(5),
            });
        }
        jTable2.setModel(tabelku);
    }catch (SQLException ex) {
        JOptionPane.showMessageDialog(rootPane, "Data yang dicari tidak di Temukan");
    }
}

    
     

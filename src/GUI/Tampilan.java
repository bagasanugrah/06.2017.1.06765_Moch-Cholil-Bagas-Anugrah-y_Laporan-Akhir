 private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {                                     
      try {
          int row =jTable2.getSelectedRow();
                  String tabel_klik=(jTable2.getModel().getValueAt(row, 0).toString());
                  java.sql.Connection koneksi =(java.sql.Connection)Koneksi.koneksiDB();
                  java.sql.Statement stm = koneksi.createStatement();
                  java.sql.ResultSet sql = stm.executeQuery("select * from pengawas where id_pengawas=''"+tabel_klik+"'");
                  if(sql.next()){
                      String id = sql.getString("id_pengawas");
                      jTextField1.setText(id);
                      String nama = sql.getString("nama_vendor");
                      jTextField2.setText(nama);
                       String proyek = sql.getString("Proyek_pekerjaan");
                      jTextField3.setText(proyek);
                      String material = sql.getString("material_yang_dibutuhkan");
                      jTextField4.setText(material);
                      String total = sql.getString("total_tagihan");
                      jTextField5.setText(total);
                  }
      }catch (Exception e){}
    }                                    
